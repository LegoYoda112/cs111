public class Array5A{
	public static void main(String[] args){
		//Creates a salary array with four elements
		double[] salary = new double[4];
		
		//Assigns values to the array
		salary[0] = 5.25;
		salary[1] = 6.55;
		salary[2] = 10.25;
		salary[3] = 16.85;
		
		//Prints out the values in the array one by one
		System.out.println("Salaries one by one are");
		System.out.println(salary[0]);
		System.out.println(salary[1]);
		System.out.println(salary[2]);
		System.out.println(salary[3]);
	}
}